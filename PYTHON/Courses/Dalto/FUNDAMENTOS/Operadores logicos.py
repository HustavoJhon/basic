# LOGICAL OPERATOS

# AND (&)
Resultado0 = True & True # Devolver True
Resultado1 = False & False # Devolvera False
Resultado2 = True & False # Devolvera False
Resultado3 = False & True # Devolvera False

# OR (|)
Resultado4 = True | True # Devolvera True
Resultado5 = False | False # Devolvera False
Resultado6 = True | False # Devolvera True
Resultado7 = False | True # Devolvera True

# NOT (not)
Resultado8 = not True #Devolver False
Resultado9= not False #Devolver True


print(f'Resultados de AND: {Resultado0}, {Resultado1}, {Resultado2}, {Resultado3}, \nResultados de  OR: {Resultado4}, {Resultado5}, {Resultado6}, {Resultado7} \nResultados de NOT: {Resultado8}, {Resultado9}')